// Q.7 Develop a program that prints the given Custom alphabetic pattern using a nested for loop.
// * * * * *
// *       *
// * * * * *
// *
// *

#include <iostream>
using namespace std;

int main() {
    
    for (int i = 0; i < 5; i++) {
        cout << "* ";
    }
    cout << endl;


    cout << "*       *" << endl;


    for (int i = 0; i < 5; i++) {
        cout << "* ";
    }
    cout << endl;
    cout << "*" << endl;
    cout << "*" << endl;

  
}

